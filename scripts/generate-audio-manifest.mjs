#!/usr/bin/env node
/**
 * Scan public/audio/<lang>/*.mp3 and emit src/data/audioManifest.js — the set of
 * item ids that have a pronunciation clip. The review runner routes a listening
 * card ONLY for ids in this set, so a card is never served for a silent item.
 *
 * Run whenever clips are (re)generated: node scripts/generate-audio-manifest.mjs
 * (pairs with `generate:audio`).
 */
import { readdirSync, writeFileSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const AUDIO_DIR = join(ROOT, "public", "audio");

const ids = [];
if (existsSync(AUDIO_DIR)) {
  for (const lang of readdirSync(AUDIO_DIR, { withFileTypes: true })) {
    if (!lang.isDirectory()) continue;
    for (const f of readdirSync(join(AUDIO_DIR, lang.name))) {
      if (f.endsWith(".mp3")) ids.push(f.slice(0, -4));
    }
  }
}
ids.sort();

const out =
  "// Auto-generated by scripts/generate-audio-manifest.mjs — do not edit by hand.\n" +
  "// The set of item ids that have a pronunciation clip in public/audio/. The\n" +
  "// review runner routes listening cards only for ids in this set.\n" +
  `export const AUDIO_IDS = new Set(${JSON.stringify(ids)});\n`;

writeFileSync(join(ROOT, "src", "data", "audioManifest.js"), out);
console.log(`Wrote ${ids.length} audio ids to src/data/audioManifest.js`);
